package org.ofbiz.order.finaccount;

public class Clasificacion {

	public int orden;
	public String clasificacionId;
	public String descripcion;
	
	public int getOrden() {
		return orden;
	}
	public void setOrden(int ordenClasificacion) {
		this.orden = ordenClasificacion;
	}
	public String getClasificacionId() {
		return clasificacionId;
	}
	public void setClasificacionId(String clasificacionId) {
		this.clasificacionId = clasificacionId;
	}
	public String getDescripcion() {
		return descripcion;
	}
	public void setDescripcion(String descripcionClasificacion) {
		this.descripcion = descripcionClasificacion;
	}
	
	
}
